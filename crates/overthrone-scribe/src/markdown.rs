//! Markdown report renderer — Produces a complete pentest report
//! in Markdown format suitable for conversion to HTML/PDF or
//! direct rendering in documentation platforms.

use crate::mapper;
use crate::narrative;
use crate::session::{EngagementSession, EvidenceType, Finding, Severity};
use chrono::Utc;
use overthrone_pilot::goals::EngagementState;

/// Render a complete Markdown report from the session
pub fn render(session: &EngagementSession) -> String {
    let mut md = String::with_capacity(32_768);

    // ── Title Page ──
    render_title_page(&mut md, session);

    // ── Table of Contents ──
    render_toc(&mut md, session);

    // ── Executive Summary ──
    md.push_str("---\n\n## 1. Executive Summary\n\n");
    md.push_str(&narrative::executive_summary(session));
    md.push_str("\n\n");
    render_severity_table(&mut md, session);

    // ── Scope ──
    md.push_str("---\n\n## 2. Scope & Objectives\n\n");
    md.push_str(&narrative::scope_description(session));
    md.push_str("\n\n");

    // ── Methodology ──
    md.push_str("---\n\n## 3. Methodology\n\n");
    md.push_str(&narrative::methodology_description());
    md.push_str("\n\n");

    // ── Findings ──
    md.push_str("---\n\n## 4. Findings\n\n");
    if session.findings.is_empty() {
        md.push_str("No findings were identified during the assessment.\n\n");
    } else {
        render_findings_summary_table(&mut md, session);
        for (i, finding) in session.findings.iter().enumerate() {
            render_finding(&mut md, finding, i + 1);
        }
    }

    // ── Attack Chain ──
    md.push_str("---\n\n## 5. Attack Chain\n\n");
    md.push_str(&narrative::attack_chain_narrative(session));
    md.push_str("\n\n");

    // ── MITRE ATT&CK Mapping ──
    md.push_str("---\n\n## 6. MITRE ATT&CK Mapping\n\n");
    render_mitre_matrix(&mut md, session);

    // ── Remediation Roadmap ──
    md.push_str("---\n\n## 7. Remediation Roadmap\n\n");
    render_remediation_roadmap(&mut md, session);

    // ── Domain Statistics ──
    md.push_str("---\n\n## 8. Domain Statistics\n\n");
    render_domain_stats(&mut md, session);

    // ── ADRecon Inventory Sections ──
    if let Some(ref state) = session.engagement_state {
        render_adrecon_inventory(&mut md, state);
    }

    // ── Appendix: Action Log ──
    md.push_str("---\n\n## Appendix A: Full Action Log\n\n");
    render_action_log(&mut md, session);

    // ── Footer ──
    md.push_str("---\n\n");
    md.push_str(&format!(
        "*Report generated by Overthrone Scribe v{} on {}*\n",
        env!("CARGO_PKG_VERSION"),
        Utc::now().format("%Y-%m-%d %H:%M UTC")
    ));

    md
}

